# [파이썬 프로그래밍 기초(2) 파이썬의 기본 응용] 2. 자료구조 -리스트, 튜플 1

'''
한 학생의 국어, 수학 점수를 튜플로 저장하고 이 튜플을 항목으로 갖는 리스트 객체가 있습니다.
이 때 첫 번째 학생은 (90, 80), 두 번째 학생은 (85, 75), 세 번째 학생은 (90, 100)의 점수를 갖습니다.
다음과 같이 결과를 만들기 위한 프로그램을 작성하십시오.

예제 출력)
1번 학생의 총점은 170점이고, 평균은 85.0입니다.
2번 학생의 총점은 160점이고, 평균은 80.0입니다.
3번 학생의 총점은 190점이고, 평균은 95.0입니다.
'''

std1, std2, std3 = (90, 80), (85, 75), (90, 100)

total1 = std1[0] + std1[1]
total2 = std2[0] + std2[1]
total3 = std3[0] + std3[1]

aver1 = total1 / 2
aver2 = total2 / 2
aver3 = total3 / 2

print('1번 학생의 총점은 {}점이고, 평균은 {}입니다.'.format(total1, aver1))
print('2번 학생의 총점은 {}점이고, 평균은 {}입니다.'.format(total2, aver2))
print('3번 학생의 총점은 {}점이고, 평균은 {}입니다.'.format(total3, aver3))